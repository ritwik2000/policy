package exavalu.com.beans;

/**
 *
 * @author Pratip
 */
public class Branch 
{
    private int branchID;
    private String branchName;
    private String branchAddress;
    private String branchEmail;
    private int branchPhone;

    /**
     * @return the branchID
     */
    public int getBranchID() {
        return branchID;
    }

    /**
     * @param branchID the branchID to set
     */
    public void setBranchID(int branchID) {
        this.branchID = branchID;
    }

    /**
     * @return the branchName
     */
    public String getBranchName() {
        return branchName;
    }

    /**
     * @param branchName the branchName to set
     */
    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }

    /**
     * @return the branchAddress
     */
    public String getBranchAddress() {
        return branchAddress;
    }

    /**
     * @param branchAddress the branchAddress to set
     */
    public void setBranchAddress(String branchAddress) {
        this.branchAddress = branchAddress;
    }

    /**
     * @return the branchEmail
     */
    public String getBranchEmail() {
        return branchEmail;
    }

    /**
     * @param branchEmail the branchEmail to set
     */
    public void setBranchEmail(String branchEmail) {
        this.branchEmail = branchEmail;
    }

    /**
     * @return the branchPhone
     */
    public int getBranchPhone() {
        return branchPhone;
    }

    /**
     * @param branchPhone the branchPhone to set
     */
    public void setBranchPhone(int branchPhone) {
        this.branchPhone = branchPhone;
    }
    
}
